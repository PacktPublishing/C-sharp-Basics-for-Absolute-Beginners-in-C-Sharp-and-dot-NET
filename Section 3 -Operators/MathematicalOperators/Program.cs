using System;

namespace MathematicalOperators
{
    class Program
    {
        static void Main(string[] args)
        {
            // C# Mathematical Operators.

            int a = 10, b = 3;
            Console.WriteLine($"a + b = {a+b}");
            Console.WriteLine($"a - b = {a-b}");
            Console.WriteLine($"a * b = {a*b}");
            Console.WriteLine($"a / b = {a/b}");

            float f = 11.0f;
            Console.WriteLine($"f / b = {f/b}");

            Console.WriteLine($"a % b = {a%b}");
        }
    }
}

using System;

namespace ComparisonOperators
{
    class Program
    {
        static void Main(string[] args)
        {
            // C# Comparison Operators

            int a = 60;

            Console.WriteLine($"a : {a}");

            Console.WriteLine($"a == 50 : {a == 50}");
            Console.WriteLine($"a != 50 : {a != 50}");
            Console.WriteLine($"a > 50 : {a > 50}");
            Console.WriteLine($"a < 50 : {a < 50}");

        }
    }
}
